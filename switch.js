let choice="hi"
switch(choice){
    case "hi":
         console.log("Hii everyone");
         break;
    case "hello":
        console.log("Hello everyone");
        break;
    case "Welcome":
        console.log("Welcome everyone");
        break;
    default:
        console.log("Nothing");            
}

//break should not be used in if else statements.
//it should be used only in loops and switch case statements